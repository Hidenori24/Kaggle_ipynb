def score_option(obs, sel, option):
    """Returns a (tier, tiebreak) tuple; higher sorts first.

    Tiers encode the intended sequencing within a single MAIN-context turn:
    evolve > attach energy > play a card > attack > retreat > pass/end. Other
    contexts (deck search, discard, coin flip, ...) mostly present only one
    option type at a time, so only the tiebreak matters there.

    Several situational overrides bump a tier out of that default order,
    checked roughly in order of urgency:
    - a lethal attack (see `attack_is_lethal`) outranks everything -- there's
      no setup worth doing instead of ending the exchange now.
    - getting a Pokemon onto a too-thin bench, or saving a low-HP active by
      retreating it (see `bench_is_thin` / `active_in_danger`), outranks
      evolve/attach when we're at risk of a Pokemon down with no backup.
    - attacking beats attaching energy once the opponent looks unprepared to
      hit back (see `opponent_underprepared`). (A "we're behind on prizes,
      so attack more" override was tried and measurably made things worse in
      self-play here -- this deck's plan is a slow evolution into a big
      attacker, so being behind means "catch up on energy," not "swing now.")
    - a big-draw Supporter beats attaching energy when the bench is thin and
      no Pokemon is in hand to put on it (see `hand_has_pokemon`) -- digging
      for a body to play is more urgent than energy that has nowhere new to
      go yet.
    - when picking a card from a search effect (Ultra Ball, Brock's
      Scouting, etc.) with the bench completely empty, a Basic Pokemon
      outranks an evolution card (see `bench_is_empty`) -- fetching another
      copy of the evolution is dead weight with no Basic in play to evolve
      it from. Checked against `bench_is_empty` rather than `bench_is_thin`
      deliberately: applying this at bench<=1 too (not just 0) measured as a
      regression in A/B testing.

    A "stop stacking energy on an already-maxed attacker, spread it to the
    bench instead" override was also tried (replays visibly showed a 4th/5th
    energy piling onto one Pokemon). It measurably lowered the win rate over
    300 seeded self-play games despite doing exactly what it was meant to do
    -- this deck's plan lives or dies on getting Mega Abomasnow ex's 3 energy
    together fast, and diverting energy elsewhere delays that more than it
    helps the bench. Removed rather than kept as a known regression.
    """
    t = option.get("type")
    ctx = sel.get("context")

    if t == OPT_END:
        return (0, 0.0)
    if t == OPT_NO:
        return (1, 0.0)
    if t == OPT_NUMBER:
        return (2, float(option.get("number") or 0))
    if t == OPT_RETREAT:
        tier = 8.7 if active_in_danger(obs) else 3
        return (tier, retreat_score(obs))
    if t == OPT_CARD:
        card = resolve_card_by_area(obs, sel, option.get("area"), option.get("index"), option.get("playerIndex"))
        val = card_value(card)
        if bench_is_empty(obs) and card and card.get("cardType") == 0 and card.get("basic"):
            # A traced real loss showed Ultra Ball fetching a *second* Mega
            # Lucario ex while the bench sat at 0 -- useless, since there was
            # no Riolu in play to evolve it from. With an empty bench, a
            # Basic that can go straight onto it now outranks fetching
            # another copy of an evolution we can't play yet.
            val += 100.0
        if ctx == CTX_DISCARD:
            return (4, -val)  # discard the least useful card
        if ctx in (CTX_SETUP_ACTIVE, CTX_TO_ACTIVE):
            # The active slot attacks first and most often, so a Pokemon
            # whose cheap attack is real damage beats one that's merely
            # bigger on paper (see cheap_reliable_damage's docstring).
            return (7, val * 0.5 + cheap_reliable_damage(card) * 1.5)
        return (7, val)  # search / switch-in / reveal: take the best card
    if t == OPT_YES:
        return (5, 1.0)
    if t == OPT_ATTACK:
        if attack_is_lethal(obs, option.get("attackId")):
            tier = 11
        elif opponent_underprepared(obs):
            tier = 8.5
        else:
            tier = 6
        return (tier, attack_score(obs, option.get("attackId")))
    if t in (OPT_PLAY, OPT_ABILITY):
        card = resolve_card_by_area(obs, sel, AREA_HAND, option.get("index"))
        is_bench_pokemon = card and card.get("cardType") == 0
        urgent = is_bench_pokemon and (bench_is_thin(obs) or active_in_danger(obs))
        text = " ".join(s.get("text", "") for s in (card.get("skills") or [])).lower() if card else ""
        # No Pokemon in hand with a thin bench is the exact dead end that
        # cost us games in replay review -- dig for one with a big draw
        # card before spending the turn on energy that won't have anywhere
        # new to go.
        digging = "draw" in text and bench_is_thin(obs) and not hand_has_pokemon(obs)
        tier = 10 if urgent else (8.6 if digging else 7)
        return (tier, card_value(card))
    if t in (OPT_ATTACH, OPT_ENERGY):
        in_play_index = option.get("inPlayIndex", 0)
        base = 40.0 - float(in_play_index)  # prefer active, then low bench slots
        hand_card = resolve_card_by_area(obs, sel, option.get("area", AREA_HAND), option.get("index"))
        target_card = CARD_DB.get(resolve_inplay_id(obs, in_play_index))
        if hand_card and target_card and hand_card.get("energyType") == target_card.get("energyType"):
            base += 5.0
        return (8, base)
    if t == OPT_EVOLVE:
        hand_card = resolve_card_by_area(obs, sel, option.get("area", AREA_HAND), option.get("index"))
        target_card = CARD_DB.get(resolve_inplay_id(obs, option.get("inPlayIndex")))
        gain = card_value(hand_card) - card_value(target_card)
        return (9, 50.0 + gain)
    return (1, 0.0)


def decide(obs):
    sel = obs.get("select")
    if sel is None:
        return load_deck()

    options = sel.get("option") or []
    n = len(options)
    if n == 0:
        return []

    min_c = sel.get("minCount") or 0
    max_c = max(sel.get("maxCount") or 0, min_c)

    scored = sorted(range(n), key=lambda i: score_option(obs, sel, options[i]), reverse=True)

    if max_c <= min_c:
        k = min_c
    else:
        top_tier, _ = score_option(obs, sel, options[scored[0]])
        k = max_c if top_tier >= 5 else min_c  # only "opt in" to optional picks when worthwhile

    k = max(min_c, min(max_c, k, n))
    return scored[:k]


