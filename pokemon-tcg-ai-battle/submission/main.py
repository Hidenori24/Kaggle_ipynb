"""Pokemon TCG AI Battle — rule-based agent.

Kaggle simulation entry point. Must define `agent(obs: dict) -> list[int]`.

Design goals (see ../docs/ENGINE_NOTES.md for the reverse-engineered `obs` schema):
  1. Never crash and never return an illegal selection — a crash or timeout is an
     instant loss, so every code path funnels through a defensive fallback.
  2. Prefer decisions driven by the real card database (`AllCard`/`AllAttack`,
     exposed by the native engine bundled in `kaggle-environments`) over blind
     heuristics, but degrade gracefully if that database can't be loaded.
  3. Reason primarily on `option[i]["type"]` (OptionType) rather than
     `select["context"]` (SelectContext), since only a subset of context codes
     could be identified empirically — the option type is what determines which
     concrete action is being offered, in every context we observed.
"""

import ctypes
import json
import os
import re

# ---------------------------------------------------------------------------
# Card / attack database (loaded once from the native engine shipped inside
# kaggle-environments; the exact same library Kaggle runs submissions against).
# ---------------------------------------------------------------------------

CARD_DB = {}
ATTACK_DB = {}


def _load_card_db():
    global CARD_DB, ATTACK_DB
    try:
        from kaggle_environments.envs.cabt.cg.sim import lib

        lib.AllCard.restype = ctypes.c_char_p
        lib.AllAttack.restype = ctypes.c_char_p
        CARD_DB = {c["cardId"]: c for c in json.loads(lib.AllCard())}
        ATTACK_DB = {a["attackId"]: a for a in json.loads(lib.AllAttack())}
    except Exception:
        CARD_DB = {}
        ATTACK_DB = {}


_load_card_db()

# ---------------------------------------------------------------------------
# Deck loading
# ---------------------------------------------------------------------------

# `__file__` is undefined when this module's source is inlined into a Jupyter
# cell (e.g. the Kaggle Notebook mirror generated by tools/build_kaggle_kernel.py)
# rather than run as a real file, so fall back to the working directory --
# load_deck() below already tolerates deck.csv not being found there.
try:
    _HERE = os.path.dirname(os.path.abspath(__file__))
except NameError:
    _HERE = os.getcwd()
DECK_CSV_PATH = os.path.join(_HERE, "deck.csv")

# Safety net baked in directly: if deck.csv is missing/corrupted at submission
# time, we still submit a legal 60-card deck instead of instant-losing.
# Kept in sync with deck.csv (see that file for card-by-card commentary).
DEFAULT_DECK = (
    [333] * 4 + [678] * 4
    + [1086] * 4 + [1121] * 4 + [1227] * 4 + [1210] * 4 + [1205] * 4 + [1163] * 4 + [123] * 4
    + [6] * 24
)


def load_deck():
    try:
        with open(DECK_CSV_PATH, "r", encoding="utf-8") as f:
            ids = [int(line.strip()) for line in f if line.strip() and not line.strip().startswith("#")]
        if len(ids) == 60:
            return ids
    except Exception:
        pass
    return list(DEFAULT_DECK)


def _own_deck_energy_ratio():
    """Fraction of our own deck that's Basic/Special Energy. Used to
    estimate the payoff of "discard N, deal X per Energy discarded"
    attacks (see attack_score) -- computed once at import time since the
    deck doesn't change mid-match."""
    try:
        deck = load_deck()
        if not deck:
            return 0.0
        energy_count = sum(1 for cid in deck if CARD_DB.get(cid, {}).get("cardType") in (5, 6))
        return energy_count / len(deck)
    except Exception:
        return 0.0


OWN_DECK_ENERGY_RATIO = _own_deck_energy_ratio()


def _own_deck_evolution_bases():
    """Names of cards that some other card in our own deck lists as its
    `evolvesFrom` -- i.e. the specific Basic(s) that actually unlock our
    deck's evolution line(s). Computed dynamically from CARD_DB/load_deck()
    at import time rather than hardcoded to any card name, consistent with
    this project's data-driven approach (see OWN_DECK_ENERGY_RATIO above).
    Used by card_value() to distinguish "a Basic with good stats" from "the
    specific Basic this deck's plan depends on" -- verified via A/B testing
    to matter once the deck runs a second Basic species (Farfetch'd): without
    this bonus, a same-HP rival Basic ties with the evolution-enabling one in
    search/discard comparisons, diverting some search hits away from it."""
    try:
        deck = load_deck()
        names = set()
        for cid in set(deck):
            card = CARD_DB.get(cid)
            if card and card.get("evolvesFrom"):
                names.add(card["evolvesFrom"])
        return names
    except Exception:
        return set()


OWN_DECK_EVOLUTION_BASES = _own_deck_evolution_bases()


