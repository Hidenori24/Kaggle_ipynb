def _fallback(obs):
    sel = obs.get("select")
    if sel is None:
        return load_deck()
    try:
        n = len(sel.get("option") or [])
        min_c = sel.get("minCount") or 0
        max_c = min(sel.get("maxCount") or 0, n)
        k = min(max(min_c, max_c if max_c >= min_c else min_c), n)
        return list(range(k))
    except Exception:
        return []


def agent(obs):
    try:
        sel = obs.get("select")
        result = decide(obs)

        if sel is None:
            return result if isinstance(result, list) and len(result) == 60 else load_deck()

        n = len(sel.get("option") or [])
        min_c = sel.get("minCount") or 0
        max_c = min(sel.get("maxCount") or 0, n)

        if not isinstance(result, list):
            raise ValueError("agent produced a non-list result")

        seen = set()
        clean = []
        for i in result:
            if isinstance(i, int) and 0 <= i < n and i not in seen:
                seen.add(i)
                clean.append(i)

        if len(clean) < min_c:
            for i in range(n):
                if i not in seen:
                    clean.append(i)
                    seen.add(i)
                if len(clean) >= min_c:
                    break
        if len(clean) > max_c:
            clean = clean[:max_c]
        return clean
    except Exception:
        return _fallback(obs)
